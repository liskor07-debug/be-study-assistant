def fetch_pk_data_pkdb(inn):
    """–°–æ–±–∏—Ä–∞–µ—Ç —Ñ–∞—Ä–º–∞–∫–æ–∫–∏–Ω–µ—Ç–∏—á–µ—Å–∫–∏–µ –¥–∞–Ω–Ω—ã–µ –∏–∑ PK-DB API."""
    BASE_URL = "https://pk-db.com/api/v1"
    params = {"cv_intra": [], "t_half": [], "cmax": [], "auc": [], "studies": []}
    
    def safe_get(url, params=None, timeout=10):
        try:
            response = requests.get(url, params=params, timeout=timeout)
            response.raise_for_status()
            return response.json()
        except Exception:
            return None

    def extract_results(data):
        if data is None:
            return []
        if isinstance(data, list):
            return data
        if isinstance(data, dict):
            return data.get("results", [])
        return []

    stats_data = safe_get(f"{BASE_URL}/statistics/substances/", params={"search": inn})
    if stats_data:
        for item in extract_results(stats_data):
            study_id = item.get("id") or item.get("study_id")
            if study_id and study_id not in params["studies"]:
                params["studies"].append(study_id)
            
            pk_params = item.get("pk_params", {})
            for key in ["cv_intra", "t_half", "cmax", "auc"]:
                val = item.get(key) or pk_params.get(key)
                if val is not None:
                    try:
                        params[key].append(float(val))
                    except (ValueError, TypeError):
                        pass
            
            if "cv" in item and item["cv"] is not None:
                try:
                    params["cv_intra"].append(float(item["cv"]))
                except (ValueError, TypeError):
                    pass
            if "half_life" in item and item["half_life"] is not None:
                try:
                    params["t_half"].append(float(item["half_life"]))
                except (ValueError, TypeError):
                    pass

    result = {}
    for key in ["cv_intra", "t_half", "cmax", "auc"]:
        if params[key]:
            valid_vals = [v for v in params[key] if isinstance(v, (int, float)) and v > 0]
            result[key] = sum(valid_vals) / len(valid_vals) if valid_vals else None
        else:
            result[key] = None
    result["studies"] = params["studies"]
    result["source"] = "PK-DB"
    return result

def fetch_pk_data_pubmed(inn):
    """–ò—â–µ—Ç PK-–ø–∞—Ä–∞–º–µ—Ç—Ä—ã –≤ PubMed —á–µ—Ä–µ–∑ E-utilities API."""
    base_url = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/"
    try:
        search_query = f"{inn}[Title/Abstract] AND (pharmacokinetics OR bioavailability OR bioequivalence OR Cmax OR AUC OR half-life)"
        search_response = requests.get(
            f"{base_url}esearch.fcgi",
            params={"db": "pubmed", "term": search_query, "retmax": 10, "retmode": "json"},
            timeout=10
        )
        
        if search_response.status_code == 200:
            search_data = search_response.json()
            pmids = search_data.get("esearchresult", {}).get("idlist", [])
            
            if pmids:
                fetch_response = requests.get(
                    f"{base_url}esummary.fcgi",
                    params={"db": "pubmed", "id": ", ".join(pmids[:3]), "retmode": "json"},
                    timeout=10
                )
                
                if fetch_response.status_code == 200:
                    fetch_data = fetch_response.json()
                    articles = []
                    for pmid in pmids[:3]:
                        if pmid in fetch_data.get("result", {}):
                            article = fetch_data["result"][pmid]
                            articles.append({
                                "pmid": pmid,
                                "title": article.get("title", ""),
                                "journal": article.get("fulljournalname", ""),
                                "pubdate": article.get("pubdate", ""),
                                "url": f"https://pubmed.ncbi.nlm.nih.gov/{pmid}/"
                            })
                    
                    return {
                        "cv_intra": None, "t_half": None, "cmax": None, "auc": None,
                        "studies": articles, "source": "PubMed"
                    }
    except Exception as e:
        st.warning(f"PubMed: {e}")

    return {"cv_intra": None, "t_half": None, "cmax": None, "auc": None, "studies": [], "source": "PubMed"}

